create table players(
    username varchar(255) primary key,
    email varchar(255) not null,
    password_hash varchar(255) not null
);

create table games(
    id auto_increment primary key,
    player1 varchar(255) not null,
    player2 varchar(255) not null,
    player3 varchar(255) not null,
    winner varchar(255) not null,
    winner_goal varchar(255) not null,
    match_year int not null,
    match_month int not null,
    match_day int not null,

    foreign key(player1) references players(username) constraint player1_fk,
    foreign key(player2) references players(username) constraint player2_fk,
    foreign key(player3) references players(username) constraint player3_fk,
    foreign key(winner) references players(username) constraint winner_fk,
    check(player1 != player2 and player1 != player3 and player2 != player3) constraint players_different,
    check(winner = player1 or winner = player2 or winner = player3) constraint winner_in_players,
    check(winner_goal in ('asia', 'africa', 'europe', 'north-america', 'south-america', 'oceania')) constraint winner_goal_valid,
    check(match_month between 1 and 12) constraint match_month_valid,
    check(match_day between 1 and 31) constraint match_day_valid
);

